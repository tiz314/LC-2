; Conta negativi e positivi
	.ORIG	x3000
	LEA	R0,A_I
	LEA	R1,A_F
	JSR	CONTA_N_P
END	BRNZP	END

A_I	.FILL	-56
	.FILL	15
	.FILL	-9
	.FILL	0
	.FILL	27
A_F	.FILL	12

; Inizio subp
CONTA_N_P
	ST	R2,BK2
	ST	R3,BK3
	ST	R4,BK4

	AND	R2,R2,#0
	AND	R3,R3,#0
	
	NOT	R1,R1
	ADD	R1,R1,#1
	
CHECK	ADD	R4,R0,R1
	BRP	FINE
	
	LDR	R4,R0,#0
	BRZP	POS
	ADD	R3,R3,#1
	BRNZP	CONT
POS	ADD	R2,R2,#1
CONT	ADD	R0,R0,#1
	BRNZP	CHECK

FINE	ADD	R1,R2,#0
	ADD	R0,R3,#0
	LD	R2,BK2
	LD	R3,BK3
	LD	R4,BK4
	RET
		


BK2	.BLKW	1
BK3	.BLKW	1
BK4	.BLKW	1

; Fine subp

	.END